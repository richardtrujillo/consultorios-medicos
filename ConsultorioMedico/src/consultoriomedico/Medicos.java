    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       CMedicoEps medico = new CMedicoEps();
       medico.setCodmedico(jTFCodMedico.getText());
       medico.setNombre(jTFNombreMed.getText());
       medico.setApellido(jTFApellidoMed.getText());
       
       // crear DAO
       
       CConsultorioDAO dao = new CConsultorioDAO();
       
       // llamar la eps con c√≥digo tal
       CEps eps = dao.getEpsById(jLEpsMedico.getSelectedValue().toString());
       medico.setTEpsCodEps(eps);
       
       dao.insertar(medico);
    }//GEN-LAST:event_jButton1ActionPerformed

